    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened

        //Tela de splash
        new Thread() {
            int i = 0;

            public void run() {
                while (i < 100) {

                    i++;
                    //mostra a porcentagem atual e implementa na barra de progresso
                    progresso.setValue(i);
                    porcentagem.setText(Integer.toString(i) + "%");

                    try {
                        sleep(20);
                    } catch (Exception ex) {

                    }

                }//fecha while

                //fechar a splash
                Loading.this.dispose();

                //chamando principal
                new ViewPrincipal().setVisible(true);
            }//fecha metodo run

        }.start();
    }//GEN-LAST:event_formWindowOpened

